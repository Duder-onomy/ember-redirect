import Ember from 'ember';
import { module, test } from 'qunit';
import startApp from '../helpers/start-app';

var App, container, lookupFunc;

module('Dynamic segments will redirect - Integration', {
  setup: function() {
    App        = startApp();
    container  = App.__container__;
    lookupFunc = container.lookup;
  },
  teardown: function() {
    Ember.run(App, App.destroy);
  }
});

// Test that a route file doesn't need to exist for a redirect to function
test('Routes generated by Ember can be redirected from', function(assert) {
  assert.expect(2);

  visit('generated').then(function() {
    var appController = lookupFunc.call(container, 'controller:application');

    assert.strictEqual(appController.get('currentPath'), 'foo', 'Register will redirect to the correct path');
    assert.strictEqual(appController.get('currentRouteName'), 'foo', 'Register will redirect to the correct route');
  });
});
