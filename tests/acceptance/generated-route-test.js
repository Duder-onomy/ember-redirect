import { test } from 'qunit';
import moduleForAcceptance from '../../tests/helpers/module-for-acceptance';

moduleForAcceptance('Dynamic segments will redirect - Integration');

// Test that a route file doesn't need to exist for a redirect to function
test('Routes generated by Ember can be redirected from', function(assert) {
  assert.expect(2);

  visit('generated').then(function() {
    assert.strictEqual(currentPath(), 'foo', 'Register will redirect to the correct path');
    assert.strictEqual(currentRouteName(), 'foo', 'Register will redirect to the correct route');
  });
});
